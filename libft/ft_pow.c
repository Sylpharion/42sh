#include "libft.h"

double		ft_pow(double x, double y)
{
	double sv;

	sv = x;
	if (x == 0)
		return (0);
	if (y == 0)
		return (1);
	while (y > 1)
	{
		x *= sv;
		y--;
	}
	while (y <= 0)
	{
		x /= sv;
		y++;
	}
	return (x);
}
